# while loop
# the loop which runs until the condition is true
'''
# for example
a = 10
while a < 20:
    print("yohooo")    # this loop will run infinitely because condition will never gonna be true
'''

#
a = 10
count = 0
while a < 20:                        # now it runs until the provided condition gets true 
    a = a + 1
    count = count + 1
    print(count,"yoho")